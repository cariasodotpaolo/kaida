                <h2>Enigmas</h2>
                <h3>A Solo Exhibition by DengCoy Miel</h3>
                <h4>9-24 JULY 2017</h4>
                <p>
                    <h4>Artist’s Notes: The Pleasures of Doing Art in a Diseased Century</h4>
                </p>
                <p>
                    Making visible the invisible is the work of an artist. The physicality of an idea, its sole provenance, comes from this. Yet, some of this meandering, generated on canvas, paper, metal and stone can be conceptually distant, placed there largely, and willfully, outside the orbit of interpretation.
                </p>
                <p>
                    What do we make of them? What do they all mean? Works that can’t be deciphered have to be invested with new meaning altogether. Investing interpretation is always the order of the day. This isn’t the sole import of artists, though, as religious charlatans can be equally rabid in their blind parroting of the sacred. Our world is a Rorschachian Paradise; hence intervention stamps a dichotomy between a personal and an “other”. The “other”, in this case, could be the imposing embodiment of an imperialist narrative, or even a slight fabrication from a homogenous source, like that which resides underneath the cloistered kimonos of old Japan. This conflation and borrowing from a supplemental source is our way of making sense of things. There’s always a hint of the postmodern in this approach.
                </p>
                <p>
                    Some mild appropriation can be employed, as seen in the Japanese “shunga” and anime component of this show. The danger here is that it renders an asynchronous thread to the work— hijacked, perhaps, by my shallow understanding of “the other”. It could be the Duchampian conceit of the ready-made at work here. In the service of our Art-making, we make full use of what is available, what is readily assimilated within the purview of culture, politics, gender, economics and whatever can be shared and appreciated by our roving constellation of eyes. We desperately need these conceptual and ideational cruxes sourced from popular culture, delivered to us faster than our hungry synapses could possibly process.
                </p>
                <p>
                    Closer to my skin, some paintings in this suite deal with the mortal bones; the materiality of paint subsuming the look and smell of rotting flesh. Yellow ochre, paynes grey, alizarin crimson and titanium white gloss over the inconsistency and the pale patina of collapsing chromosomes— reinvigorating the flesh where this fragile vessel of blackened meat and bones used to be. We celebrate death and we celebrate life even more so.
                </p>
                <p>
                    I celebrate Painting.
                </p>
                <p>
                    The 20 works in this show are premeditations for larger ones, a crucial step in the process. These are complete studies to be explored further on bigger canvases, tackling and elevating the themes and ideational constructs. By then, I hope to make sense of these forbidden, conflicted and delicious conundrums and effect a sense of nowness onto them relevant to our future histories.
                </p>
