                <h2>#artfusion!</h2>
                <h3>A Group Exhibition featuring Rene Cuvos, Demet Dela Cruz, Darwin Guevarra, Philipp Ines, Jim Orencio, Migi Sebastian, Mike Seiling and Manuel Sintos</h3>
                <h4>9 JULY 2017</h4>
                <p>
                Fusion and Dissociation
                </p>
                <p>
                In #artfusion!, low brow art is presented side by side with calming garden views, steampunk automatons, pensive figures, childhood dreams and dynamic abstracts, as steely creatures in flight, mixed media work spiked with LED lights and a video installation dot the gallery space. These create a heterogenous array of distinctive artworks in synthesis. By representing diverse media through their personal styles, the artists are given a free reign on the individuation and exploration of their own concerns and concepts, leading to a cohesive cross-sampling of disciplines in one engaging visual sweep.
                </p>
                <p>
                Flight seems to be a common ground, with several pieces centered on the subject. Migi Sebastian’s Kinetic Sculptures take on avian and arthropod forms in equilibrium with assembled scraps of metal and gears, some of them in motion. Philipp Ines’ Guni-Guni takes advantage of childhood play and imagination with oil and pencil on canvas as he recreates the illusion of corrugated cardboard against a clear blue sky. This contrasts nicely with Manuel Sintos’ Pilot 1, an oil on canvas painting illustrating a boy with toy helicopters and planes with an overlay of text that seems to underscore the longing for freedom and aerial navigation.
                Automation and mobility may also be held as a theme in Darwin Guevarra’s paintings, Midnight Blue Traveler and The Cart King. Jalopy parts are put together with different elements such as horns, gears, metal components and even fabric curtains, resulting into contraptions that proclaim the virtues of a nomadic life. Demet dela Cruz’ Fire Starter, with oil on wood, mixed media and LED lights, make up a powerful and beautifully-rendered saddled horse proudly marching into battle with classical figures populating its pedestal and a melee raging right behind it. For Mike Seiling, an acrylics on canvas diptych, Austere Illusions, might be likened to an alien satellite, with elements spewing out streams of color and a queue of familiar shapes disappearing into the distance with an economy of lines and embellishment. His video installation, Discombobulated Entities, bears the same minimalism and sensitivity.
                Cuvos’ subject might an antlered bear either existing in symbiosis with colorful birds, or a predator biding his time in As We Will, So Shall It Be Done. The creatures appear calm and comfortable in their natural habitat, notwithstanding the weird assembly of body parts. This fantastic sight is offset by Lilim, Jim Orencio’s sawdust and acrylics on canvas painting that speaks of calm respite, quiet moments and a pocketful of sunshine as one breathes in nature, positive energies enveloping us as a force field against the turmoils of daily living.
                While a rich spectrum of interesting juxtapositions are produced with the combination of the works in #artfusion!, the praxis brought forth by each artist differentiates himself from the rest in intent, treatment and execution, making this amalgamation of forms within the show truly an intriguing blend.
                </p>
                <p>
                Kaye O’Yek<br>
                July 2017
                </p>

